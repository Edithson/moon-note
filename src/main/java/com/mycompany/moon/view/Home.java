            // L'utilisateur a cliqu√© sur "Ouvrir" ou "S√©lectionner"

            // R√©cup√©rer le fichier s√©lectionn√©
            java.io.File selectedFile = fileChooser.getSelectedFile();

            // Afficher le chemin (pour v√©rification)
            String filePath = selectedFile.getAbsolutePath();
            System.out.println("Fichier s√©lectionn√© : " + filePath);

            // üöÄ Lancer l'importation
            String report = ImportManager.importDatabase(filePath);

            // Afficher le rapport
            JOptionPane.showMessageDialog(this, report, "Rapport d'Importation", JOptionPane.INFORMATION_MESSAGE);

